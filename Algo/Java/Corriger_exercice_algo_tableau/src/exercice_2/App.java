package exercice_2;

public class App {
	/*
	Variables 
	tab2[6] est un tableau de caractere
Debut du programme
	tab2[0] <-- "a"
	tab2[1] <-- "e"
	tab2[2] <-- "i"
	tab2[3] <-- "o"
	tab2[4] <-- "u"
	tab2[5] <-- "y"
Fin du programme
	 */

	public static void main(String[] args) {
		// declaration d'un tableau de caractère contenant les voyelles de l'alphabet
		char[] tab2 = new char[6];
		
		// rempissage de chaque élément du tableau par une lettre de l'alphabet en précisant l'index de l'élément
		tab2[0] = 'a';
		tab2[1] = 'e';
		tab2[2] = 'i';
		tab2[3] = 'o';
		tab2[4] = 'u';
		tab2[5] = 'y';
		

	}

}
